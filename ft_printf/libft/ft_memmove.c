#include "libft.h"
#include <stddef.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (d == s || len == 0)
		return (dst);
	if (d < s)
		return (ft_memcpy(dst, src, len));
	while (len > 0)
	{
		len--;
		d[len] = s[len];
	}
	return (dst);
}
